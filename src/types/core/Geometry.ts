
/**
 * Point interface
 */
export interface Point {
  x: number;
  y: number;
}

/**
 * Size interface
 */
export interface Size {
  width: number;
  height: number;
}

/**
 * Rectangle interface
 */
export interface Rectangle {
  x: number;
  y: number;
  width: number;
  height: number;
}

/**
 * Circle interface
 */
export interface Circle {
  x: number;
  y: number;
  radius: number;
}

/**
 * Line interface
 */
export interface Line {
  x1: number;
  y1: number;
  x2: number;
  y2: number;
}
